#include "Fort.h"
Fort::Fort(SDL_Surface *screen, int posx, int posy) {
    this->screen = screen;
    this->posx = posx;
    this->posy = posy;
    this->current_frame = 0;
    Animation *fort_alive = new Animation(this->screen,
                       "/home/matias/Escritorio/Client/sprites/fuertes/alive.bmp",
                                     160,192);
    this->animation.push_back(fort_alive);
    Animation*fort_death = new Animation(this->screen,
                                    "/home/matias/Escritorio/Client/sprites/fuertes/death.bmp",
                                    160,192);
    this->animation.push_back(fort_death);
}

